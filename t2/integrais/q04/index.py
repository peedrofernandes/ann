def trapz(x, y):
  soma = 0

  n = len(x)

  for i in range(1, n):
    b1 = y[i - 1]
    b2 = y[i]
    h = abs(x[i] - x[i - 1])
    soma += (b1 + b2) * h / 2
  
  return soma

